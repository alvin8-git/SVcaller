#!/usr/bin/env python3
"""Generate the globin pathogenic-site panels from curated HGVS + a gene model.

Panels are DERIVED, never hand-typed: every genomic coordinate comes from
hgvs_map.py, and every row is cross-checked against the reference FASTA. On a
minus-strand gene (HBB) the coding reference base is the complement of the
genomic base; if the FASTA disagrees with the curated allele, the row is wrong
and the run fails rather than emitting a panel that silently calls nothing.

    python3 bin/make_globin_panels.py --ref /path/hg38.fa --outdir assets/

Curation note: the HGVS strings below are the *inputs* — they encode literature
allele definitions and are the part a human must review. Everything downstream
is computed. Review HGVS + allele name; do not review coordinates by eye.
"""
import argparse
import os
import subprocess
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from hgvs_map import load_models, DEFAULT_BED  # noqa: E402

COMP = {"A": "T", "C": "G", "G": "C", "T": "A"}

# gene, allele, hgvs_pos, coding_ref, coding_alt, event, population, note
HBB_SITES = [
    ("HBB", "HbS",         "20",      "A", "T", "snv", "AFR",     "sickle cell; c.20A>T p.Glu7Val"),
    ("HBB", "HbC",         "19",      "G", "A", "snv", "AFR",     "c.19G>A p.Glu7Lys"),
    ("HBB", "HbE",         "79",      "G", "A", "snv", "SEA,SAS", "commonest SE Asian beta allele"),
    ("HBB", "CD17",        "52",      "A", "T", "snv", "EAS",     "c.52A>T p.Lys18Ter, beta-0"),
    ("HBB", "CD39",        "118",     "C", "T", "snv", "MED",     "c.118C>T p.Gln40Ter, beta-0"),
    ("HBB", "CD41-42",     "126",     "-", "-", "del4", "EAS",    "c.126_129delCTTT, beta-0; commonest S-Chinese"),
    ("HBB", "CD71-72",     "216",     "-", "-", "ins1", "EAS",    "c.216_217insA, beta-0"),
    ("HBB", "IVS-I-1",     "92+1",    "G", "A", "snv", "MED,SAS", "splice donor, beta-0"),
    ("HBB", "IVS-I-5",     "92+5",    "G", "C", "snv", "SAS,SEA", "splice donor region, beta+"),
    ("HBB", "IVS-I-110",   "93-21",   "G", "A", "snv", "MED",     "cryptic splice, beta+"),
    ("HBB", "IVS-II-654",  "316-197", "C", "T", "snv", "EAS",     "cryptic splice, beta+; common Chinese"),
    # "-28" is numbered from the CAP site, not the ATG. HBB has a 50 nt 5' UTR,
    # so CAP-28 == c.-78. Confirmed against the FASTA: c.-78 sits in the CATAAAA
    # TATA box (chr11:5227097-5227103) and carries coding A, as the allele requires.
    ("HBB", "-28 (TATA)",  "-78",     "A", "G", "snv", "EAS,SEA", "TATA box, beta+; CAP-relative name"),
]

HBA_SITES = [
    ("HBA2", "Hb Constant Spring", "427", "T", "C", "snv", "SEA", "stop-codon readthrough; commonest SE Asian non-deletional"),
    ("HBA2", "Hb Quong Sze",       "377", "T", "C", "snv", "EAS", "p.Leu126Pro; observed het in THAL2"),
    ("HBA2", "Hb Adana",           "179", "G", "A", "snv", "SEA", "p.Gly60Asp; severe, also occurs on HBA1"),
    ("HBA1", "Hb Adana (HBA1)",    "179", "G", "A", "snv", "SEA", "same substitution on the HBA1 paralogue"),
]

HEADER = [
    "gene", "allele", "hgvs_c", "chrom", "pos", "strand",
    "coding_ref", "coding_alt", "genomic_ref", "genomic_alt",
    "event", "population", "transcript", "note",
]


def faidx_base(ref, chrom, pos):
    out = subprocess.run(["samtools", "faidx", ref, f"{chrom}:{pos}-{pos}"],
                         capture_output=True, text=True, check=True).stdout
    return "".join(l.strip() for l in out.splitlines()[1:]).upper()


def build(sites, models, ref):
    rows, problems = [], []
    for gene, allele, hgvs, cref, calt, event, pop, note in sites:
        m = models[gene]
        try:
            pos = m.resolve(hgvs)
        except ValueError as e:
            problems.append(f"{gene} {allele} c.{hgvs}: {e}")
            continue
        gref_obs = faidx_base(ref, m.chrom, pos)
        if cref == "-":                       # indel: only the anchor is checked
            gref, galt = gref_obs, "-"
        else:
            gref = cref if m.strand == "+" else COMP[cref]
            galt = calt if m.strand == "+" else COMP[calt]
            if gref_obs != gref:
                problems.append(
                    f"{gene} {allele} c.{hgvs} at {m.chrom}:{pos}: FASTA has "
                    f"{gref_obs}, curated allele implies {gref} — coordinate or allele is wrong")
                continue
        rows.append([gene, allele, f"c.{hgvs}", m.chrom, str(pos), m.strand,
                     cref, calt, gref, galt, event, pop, m.tx, note])
    return rows, problems


def write(path, rows, title, extra):
    with open(path, "w") as fh:
        fh.write(f"# {title}\n")
        fh.write("# GENERATED by bin/make_globin_panels.py - do not hand-edit coordinates.\n")
        fh.write("# Coordinates derived from RefSeq gene models via bin/hgvs_map.py and\n")
        fh.write("# cross-checked against the reference FASTA. Review the allele/HGVS\n")
        fh.write("# curation in that script; never review coordinates by eye.\n")
        for line in extra:
            fh.write(f"# {line}\n")
        fh.write("#\n")
        fh.write("# coding_ref/alt are on the TRANSCRIPT strand; genomic_ref/alt are what a\n")
        fh.write("# pileup or VCF will actually show. HBB is minus-strand: they differ.\n")
        fh.write("# For indels, pos is the first affected coding base; a VCF will anchor one\n")
        fh.write("# base away per its own convention - do not compare positions naively.\n")
        fh.write("\t".join(HEADER) + "\n")
        for r in rows:
            fh.write("\t".join(r) + "\n")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--ref", default="/data/alvin/ref/GRCh38/hg38.fa")
    ap.add_argument("--bed", default=DEFAULT_BED)
    ap.add_argument("--outdir", default="assets")
    a = ap.parse_args()

    models = load_models(a.bed, {"HBB", "HBA1", "HBA2"})
    all_problems = []

    hbb, p1 = build(HBB_SITES, models, a.ref)
    hba, p2 = build(HBA_SITES, models, a.ref)
    all_problems += p1 + p2

    if all_problems:
        print("PANEL GENERATION FAILED - refusing to write a panel with bad rows:", file=sys.stderr)
        for p in all_problems:
            print(f"  {p}", file=sys.stderr)
        sys.exit(1)

    write(os.path.join(a.outdir, "hbb_pathogenic_sites.tsv"), hbb,
          "HBB (beta-globin) pathogenic site panel - GRCh38",
          ["Consumed by OmniGen from the germline VCF. HBB is not in a segmental",
           "duplication, so ordinary SNV calls here are reliable - this panel is a",
           "lookup list, not a caller.",
           "NOT a complete beta-thal panel: ~280 alleles are known and this covers the",
           "common ones by population. Absence of a hit is NOT a negative result."])
    write(os.path.join(a.outdir, "hba_pathogenic_sites.tsv"), hba,
          "HBA1/HBA2 (alpha-globin) pathogenic site panel - GRCh38",
          ["Consumed by SVcaller channel 4 as a targeted pileup at fixed positions.",
           "NOT variant discovery - nothing outside this list is examined.",
           "Zygosity is copy-number dependent: on a --SEA background the surviving",
           "HBA2 is hemizygous, so a real variant sits near 100% VAF, not 50%.",
           "Interpret only alongside the alpha gene count from channel 1."])

    print(f"hbb_pathogenic_sites.tsv  {len(hbb)} sites")
    print(f"hba_pathogenic_sites.tsv  {len(hba)} sites")
    print("all rows cross-checked against the reference FASTA")


if __name__ == "__main__":
    main()
